    <h2>Basic Alerts <small>Wrap any text and an optional dismiss button in '.alert' and one of the four contextual classes (e.g., .alert-success) for basic alert messages.</small></h2>
</div>
<div role="alert" class="alert alert-success"><i class="zmdi zmdi-check"></i><strong class="alert-title">Well done!</strong> You successfully read this important alert message.</div>
<div role="alert" class="alert alert-info"><i class="zmdi zmdi-comment-outline"></i><strong class="alert-title">Heads up!</strong> This alert needs your attention, but it's not super important.</div>
<div role="alert" class="alert alert-warning"><i class="zmdi zmdi-notifications"></i><strong class="alert-title">Warning!</strong> Better check yourself, you're not looking too good.</div>
<div role="alert" class="alert alert-danger"><i class="zmdi zmdi-close"></i><strong class="alert-title">Oh snap!</strong> Change a few things up and try submitting again.</div>

